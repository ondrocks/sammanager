package pl.edu.agh.samm.eclipse.commands;

import java.util.Properties;

import org.eclipse.core.commands.AbstractHandler;
import org.eclipse.core.commands.ExecutionEvent;
import org.eclipse.core.commands.ExecutionException;
import org.eclipse.core.runtime.preferences.InstanceScope;
import org.osgi.service.prefs.Preferences;

import pl.edu.agh.samm.eclipse.SAMM;

/**
 * @author Pawel Koperek <pkoperek@gmail.com>
 * @author Mateusz Kupisz <mkupisz@gmail.com>
 * 
 */
public class GetWeightsPropertiesCommand extends AbstractHandler {

	public static final String ID = "pl.edu.agh.samm.eclipse.getweightproperties";

	@Override
	public Object execute(ExecutionEvent event) throws ExecutionException {
		Properties props = new Properties();
		Preferences preferences = new InstanceScope().getNode(SAMM.PLUGIN_ID);

		Preferences weights = preferences.node("weights");
		props.put("weight.user.rank", weights.get("weight.user.rank", "0.35"));
		props.put("weight.specifity", weights.get("weight.specifity", "0.35"));
		props.put("weight.manual", weights.get("weight.manual", "0.20"));
		props.put("weight.auto", weights.get("weight.auto", "0.10"));

		return props;

	}

}
