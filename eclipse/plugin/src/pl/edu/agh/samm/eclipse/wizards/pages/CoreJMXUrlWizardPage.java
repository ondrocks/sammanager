package pl.edu.agh.samm.eclipse.wizards.pages;

import org.eclipse.jface.wizard.WizardPage;
import org.eclipse.swt.SWT;
import org.eclipse.swt.layout.GridData;
import org.eclipse.swt.layout.GridLayout;
import org.eclipse.swt.widgets.Composite;
import org.eclipse.swt.widgets.Label;
import org.eclipse.swt.widgets.Text;

/**
 * @author Pawel Koperek <pkoperek@gmail.com>
 * @author Mateusz Kupisz <mkupisz@gmail.com>
 * 
 */
public class CoreJMXUrlWizardPage extends WizardPage {

	private Composite container;
	private Text text1;

	public CoreJMXUrlWizardPage() {
		super("SAMM core JMX URL");
		setTitle("SAMM core JMX URL");
		setDescription("Insert SAMM's core JMX URL");
	}

	@Override
	public void createControl(Composite parent) {
		container = new Composite(parent, SWT.NULL);
		GridLayout layout = new GridLayout();
		container.setLayout(layout);
		layout.numColumns = 2;
		Label label1 = new Label(container, SWT.NULL);
		label1.setText("SAMM's JMX URL:");

		text1 = new Text(container, SWT.BORDER | SWT.SINGLE);
		text1.setText("service:jmx:rmi:///jndi/rmi://localhost:9999/jmxrmi");
		GridData gd = new GridData(GridData.FILL_HORIZONTAL);
		text1.setLayoutData(gd);
		// Required to avoid an error in the system
		setControl(container);

	}

	public String getJmxUrl() {
		return text1.getText();
	}

}
