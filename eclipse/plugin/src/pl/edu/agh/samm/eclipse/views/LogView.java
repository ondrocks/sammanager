package pl.edu.agh.samm.eclipse.views;

import org.eclipse.swt.SWT;
import org.eclipse.swt.layout.FillLayout;
import org.eclipse.swt.widgets.Composite;
import org.eclipse.swt.widgets.Text;
import org.eclipse.ui.part.ViewPart;

/**
 * @author Pawel Koperek <pkoperek@gmail.com>
 * @author Mateusz Kupisz <mkupisz@gmail.com>
 * 
 */
public class LogView extends ViewPart {

	public static String ID = "pl.edu.agh.samm.eclipse.views.LogView";
	private Text logText;

	public LogView() {
		// TODO Auto-generated constructor stub
	}

	@Override
	public void createPartControl(Composite parent) {
		Composite root = new Composite(parent, SWT.NULL);
		root.setLayout(new FillLayout(SWT.VERTICAL));
		logText = new Text(root, SWT.MULTI);
		logText.setEditable(false);

		logText.setText("Some logged Text\nNext line");
	}

	@Override
	public void setFocus() {
		// TODO Auto-generated method stub

	}

}
